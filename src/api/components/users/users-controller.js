const usersService = require('./users-service');
const { errorResponder, errorTypes } = require('../../../core/errors');

/**
 * Handle get list of users request
 * @param {object} request - Express request object
 * @param {object} response - Express response object
 * @param {object} next - Express route middlewares
 * @returns {object} Response object or pass an error to the next route
 */
async function getUsers(request, response, next) {
  try {
    const { page_number, page_size, search, sort } = request.query;

    // Penanganan query parameter page_number dan page_size
    const pageNumber = parseInt(page_number) || 1;
    const pageSize = parseInt(page_size) || 10;

    let filter = {};
    if (search) {
      const searchRegex = /^(email|name):(.+)$/i;
      const match = search.match(searchRegex);
      if (match) {
        const fieldName = match[1].toLowerCase();
        const searchKey = match[2];

        if (fieldName === 'email' || fieldName === 'name') {
          filter[fieldName] = { $regex: searchKey, $options: 'i' };
        } else {
          console.log(
            'Invalid search attribute. Default search will be applied.'
          );
        }
      } else {
        console.log('Invalid search format. Default search will be applied.');
      }
    }

    let sortOptions = {};
    if (sort) {
      const sortRegex = /^(email|name)\s*:\s*(asc|desc)$/i;
      const match = sort.match(sortRegex);
      if (match) {
        const fieldName = match[1].toLowerCase();
        const sortOrder = match[2].toLowerCase();
        sortOptions[fieldName] = sortOrder === 'desc' ? -1 : 1;
      } else {
        console.log(
          'Invalid sort format. Default sorting by email ascending will be applied.'
        );
      }
    } else {
      console.log(
        'Sort parameter not provided. Default sorting by email ascending will be applied.'
      );
    }

    const { users, count, total_pages, has_previous_page, has_next_page } =
      await usersService.getUsersPaginated(
        pageNumber,
        pageSize,
        filter,
        sortOptions
      );

    return response.status(200).json({
      page_number: pageNumber,
      page_size: pageSize,
      count,
      total_pages,
      has_previous_page,
      has_next_page,
      data: users,
    });
  } catch (error) {
    return next(error);
  }
}

